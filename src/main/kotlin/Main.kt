package org.example

fun main() {
    // Integer tests
    val verifier = IntegerVerifier()
    println("Integer tests")
    println("Valid:")
    println(verifier.verify("12345"))
    println(verifier.verify("12345455469785968990234902190398082883482384848"))
    println("Invalid:")
    println(verifier.verify("0123456768798"))
    println(verifier.verify("0"))
    println(verifier.verify("123abc123"))
    println("-----------------")
    // Float tests
    val floatVerifier = FloatVerifier()
    println("Float tests")
    println("Valid:")
    println(floatVerifier.verify("12345"))
    println(floatVerifier.verify("1.01"))
    println(floatVerifier.verify("0.01"))
    println(floatVerifier.verify(".01"))
    println(floatVerifier.verify("15.25"))
    println(floatVerifier.verify(".1"))
    println("Invalid:")
    println(floatVerifier.verify("0123456768798"))
    println(floatVerifier.verify("123abc123"))
    println(floatVerifier.verify("0.10."))
    // Binary tests
    val binaryVerifier = BinaryVerifier()
    println("Binary tests")
    println("Valid:")
    println(binaryVerifier.verify("1"))
    println(binaryVerifier.verify("11"))
    println(binaryVerifier.verify("101"))
    println(binaryVerifier.verify("11111111"))
    println(binaryVerifier.verify("100011010001"))
    println(binaryVerifier.verify("1001"))
    println("Invalid:")
    println(binaryVerifier.verify("0123456768798"))
    println(binaryVerifier.verify("123abc123"))
    println(binaryVerifier.verify("0.10."))
    println(binaryVerifier.verify("0"))
    println(binaryVerifier.verify("01"))
    println(binaryVerifier.verify("100"))
    println(binaryVerifier.verify("10"))
}